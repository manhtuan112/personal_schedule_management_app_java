    private void jButton_SelectImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SelectImageActionPerformed
        // TODO add your handling code here:
        //Chon anh va set link anh vao phan image path
        //thiet lap bien de lay link anh trong phan dau cua code
        String path = null;
        //tao file lua chon
        JFileChooser chooser = new JFileChooser();
        //thiet lap thu muc lua chon
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        //set phan mo rong file de them vao phan loc
        FileNameExtensionFilter extension = new FileNameExtensionFilter("*Image","jpg","png","jpef");
        chooser.addChoosableFileFilter(extension);
        
        //hient hi hop thoai luu
        int filestate = chooser.showSaveDialog(null);
        
        //kiem tra link anh
        
        if(filestate == JFileChooser.APPROVE_OPTION){
            File selectedImage = chooser.getSelectedFile();
            path = selectedImage.getAbsolutePath();
            
            jLabel_imagepath.setText(path);
            image_path = path;
            //set anh cho label va doi kich thuoc
            try {
                BufferedImage image = ImageIO.read(new File(path));
                if(image==null){
                    JOptionPane.showMessageDialog(this, "Error loading image", "Error!", JOptionPane.ERROR_MESSAGE);
                }
                else{
                    int x = jLabel_image.getSize().width;
                    int y = jLabel_image.getSize().height;
                    ImageIcon icon = new ImageIcon(image.getScaledInstance(x, y, image.SCALE_SMOOTH));
                    jLabel_image.setIcon(icon);
                }   
            } catch (IOException ex) {
                Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
            
            }
        }
        
        
    }//GEN-LAST:event_jButton_SelectImageActionPerformed

