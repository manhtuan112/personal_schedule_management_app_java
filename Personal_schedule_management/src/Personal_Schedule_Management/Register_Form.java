    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed
        // TODO add your handling code here:
        
        
        String fname = jTextField_Fullname.getText();
        String username = jTextField_Username.getText();
        String pass1 = String.valueOf(jPasswordField_1.getPassword());
        String pass2 = String.valueOf(jPasswordField_2.getPassword());
        String phone = jTextField_Phone.getText();
        String gender = "Male";
        
        if(jRadioButton_Female.isSelected()){
            gender = "Female";
        }
        if(vetifyField()){
            if(!checkUsername(username)){
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery = "INSERT INTO `users`(`full_name`, `username`, `password`, `phone`, `gender`, `picture`, `birthdate`, `fsaying`, `idstudent`) VALUES (?,?,?,?,?,?,?,?,?)";
                
                try {
                    ps = My_CNX.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1, fname);
                    ps.setString(2, username);
                    ps.setString(3, pass1);
                    ps.setString(4, phone);
                    ps.setString(5, gender);
                    
                    //luu anh nhu 1 blog trong database
                    try {
                        if(image_path != null){
                            InputStream image = new FileInputStream(new File(image_path));
                            ps.setBlob(6, image);
                        }
                        else{
                            ps.setNull(6, java.sql.Types.NULL);
                        }
                        ps.setNull(7, java.sql.Types.NULL);
                        ps.setNull(8, java.sql.Types.NULL);
                        ps.setNull(9, java.sql.Types.NULL);
                        
                        if(ps.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(this, "Your Account Has Been Create");
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "Error: Check Your Information");
                        }
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    
                } catch (SQLException ex) {
                    Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                
            
            }
        }
        
    }//GEN-LAST:event_jButton_RegisterActionPerformed

