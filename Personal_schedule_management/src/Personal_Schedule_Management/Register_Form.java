    public boolean vetifyField(){
        String fname = jTextField_Fullname.getText();
        String uname = jTextField_Username.getText();
        String phone = jTextField_Phone.getText();
        String pass1 = String.valueOf(jPasswordField_1.getPassword());
        String pass2 = String.valueOf(jPasswordField_2.getPassword());
        
        //kiem tra o trong
        if(fname.trim().equals("")||uname.trim().equals("")||phone.trim().equals("")
                || pass1.trim().equals("")||pass2.trim().equals("")){
            JOptionPane.showMessageDialog(this, "One and more are empty", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else
             //kiem tra 2 mau khau co giong nhau khong
             if(!pass1.trim().equals(pass2.trim())){
                    JOptionPane.showMessageDialog(this, "Password doesn't Match", "Confirm Password",JOptionPane.ERROR_MESSAGE);
                    return false;
              }
             else{
                 return true;
             }
        //neu dung het
        
        
    }
    
