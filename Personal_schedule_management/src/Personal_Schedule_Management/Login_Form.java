    private void jPasswordFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordFieldFocusLost
        //neu doan Text trong password la trong hoac bang ****** thi set "******" cho Text Field
        String pass = String.valueOf(jPasswordField.getPassword());
        if(pass.trim().toLowerCase().equals("******")
            ||pass.trim().toLowerCase().equals("")){
            jPasswordField.setText("******");
            jPasswordField.setForeground(new Color(153,153,153));
        }
        //xoa hop cho icon
        //jLabel_password.setBorder(null);
        //tao hop nhat mau cho icon
        Border label_icon_border = BorderFactory.createMatteBorder(1,1,1,1, new Color(153,153,153));
        jLabel_password.setBorder(label_icon_border);
    }//GEN-LAST:event_jPasswordFieldFocusLost

