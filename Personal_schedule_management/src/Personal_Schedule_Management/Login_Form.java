    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        // TODO add your handling code here:
        PreparedStatement st;
        ResultSet rs;

        //Nhan username va password
        String username = jTextField_Username.getText();
        String password = String.valueOf(jPasswordField.getPassword());

        //
        String query = "SELECT * FROM `users` WHERE `username` = ?  AND `password` = ?";
     
        if(username.trim().toLowerCase().equals("username")){
            JOptionPane.showMessageDialog(this, "Enter your Username", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(password.trim().toLowerCase().equals("******")){
            JOptionPane.showMessageDialog(this, "Enter your Password", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try {
            st = My_CNX.getConnection().prepareStatement(query);
            st.setString(1, username);
            st.setString(2,password);
            rs = st.executeQuery();

            if(rs.next()){
                //Vao form menu
                String name = rs.getString(2);
                String user = rs.getString(3);
                String id = rs.getString(10);
                String phone = rs.getString(5);
                Date date;
               
                if(rs.getDate(8)==null){
                    date = null;
                }
                
                else{
                    date = rs.getDate(8);
                }
                String fsaying = rs.getString(9);
                
                count++;
                String path = "D:\\"+count+".png";
                Blob fileData = rs.getBlob(7);
                if(fileData==null){
                    path = null;
                }
                else{
                    InputStream is = fileData.getBinaryStream();
                    try {
                        copyInputStreamToFile(is, new File(path));
                    } catch (IOException ex) {
                        Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
                
                
                Menu_Form form = new Menu_Form(name, user, path, id, phone, date, fsaying);
                form.setVisible(true);//set menu hien thi   
                form.pack();//giup menu hien thi voi kich thuoc vua du so voi noi dung menu
                form.setLocationRelativeTo(null); //set menu chinh giua man hinh
                //dong form dang nhap
                this.dispose();

            }
            else{
                //hien thi thong bao loi
                JOptionPane.showMessageDialog(this, "Invalid Usernames / Password", "Login Error", JOptionPane.WARNING_MESSAGE);//tao cau lenh thong bao loi
            }
            } catch (SQLException ex) {
            Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        

    }//GEN-LAST:event_jButton_LoginActionPerformed

